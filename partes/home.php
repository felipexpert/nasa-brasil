          <h3>Teoria Especial da Relatividade</h3>
          <p>(Albert Einstein, 1905)</p>
        </header>
        <p>O Universo nunca mais foi o mesmo depois de 1905. Naquele ano, o alemão Albert Einstein (1879-1955), um obscuro funcionário do escritório de patentes de Berna, na Suíça, saiu do casulo com a publicação de seis textos científicos. Todos foram muito importantes. Mas dois deles, nos quais expõe a Teoria Especial da Relatividade, tornaram Einstein o maior gênio da ciência no século XX. Segundo ele, o tempo e o espaço não são categorias absolutas, como se imaginava até então, mas dependem da posição e da velocidade em que se encontra o observador. Daí o nome – relatividade. </p>
        <p>Só é imutável a velocidade da luz, de 300 000 quilômetros por segundo. Nada, demonstrou Einstein, pode viajar mais depressa do que ela.</p>
